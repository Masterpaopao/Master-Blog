# 更好地打印信息，以装饰器修饰一个完整的操作周期
def printf(func):
    """
    功能：
        以装饰器的形式打印一个操作周期里面的所有输出，看上去更直观
    参数：
        e - 打印内容
    """
    def inner(*args, **kwargs):
        print("---------------------------------------------------------")
        func(*args, **kwargs)
        print("---------------------------------------------------------")
    return inner
